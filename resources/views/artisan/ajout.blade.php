            priceInput.addEventListener('input', (e) => {
                const value = e.target.value || 0;
                const commission = value * 0.05;
                const total = value - commission;

                netIncome.textContent = `${Math.floor(total).toLocaleString()} FCFA`;
                commissionDisplay.nextElementSibling.textContent = `- ${Math.floor(commission).toLocaleString()} FCFA`;
